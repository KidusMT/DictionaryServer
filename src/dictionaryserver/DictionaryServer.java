    public void initMainThread() {

        List keys = new ArrayList(this.dictionary.keySet());

        DefaultListModel model = new DefaultListModel();

        for (int i = 0; i < keys.size(); i++) {
            model.addElement(keys.get(i));
        }
        
        jList1.setModel(model);
        jList1.add(new JScrollPane());

        // this looks like a blocking call
        this.dictServer.start();
        System.out.println("dictServer started");
    }

